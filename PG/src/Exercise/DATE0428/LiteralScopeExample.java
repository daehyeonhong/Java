package Exercise.DATE0428;

public class LiteralScopeExample {
	public static void main(String[] args) {
		System.out.println(Byte.MIN_VALUE+"<=범위<="+Byte.MAX_VALUE);
		System.out.println(Short.MIN_VALUE+"<=범위<="+Short.MAX_VALUE);
		System.out.println('\u0000'+"<=범위<="+'\uffff');
		System.out.println(Integer.MIN_VALUE+"<=범위<="+Integer.MAX_VALUE);
		System.out.println(Long.MIN_VALUE+"<=범위<="+Long.MAX_VALUE);
		System.out.println(Float.MIN_VALUE+"<=범위<="+Float.MAX_VALUE);
		System.out.println(Double.MIN_VALUE+"<=범위<="+Double.MAX_VALUE);
		
	}
}
