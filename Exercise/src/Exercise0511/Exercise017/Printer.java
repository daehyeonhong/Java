package Exercise0511.Exercise017;

public class Printer {
	static int intVar;
	static boolean boolVar;
	static double dobVar;
	static String StrVar;

	static void println(int intVar) {
		System.out.println(intVar);
	}

	static void println(boolean boolVar) {
		System.out.println(boolVar);
	}

	static void println(double dobVar) {
		System.out.println(dobVar);
	}

	static void println(String strVar) {
		System.out.println(strVar);
	}
}