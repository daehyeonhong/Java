package Exercise0511.Exercise016;

public class Printer {
	int intVar;
	boolean boolVar;
	double dobVar;
	String StrVar;

	public Printer() {
	}

	void println(int intVar) {
		System.out.println(intVar);
	}

	void println(boolean boolVar) {
		System.out.println(boolVar);
	}

	void println(double dobVar) {
		System.out.println(dobVar);
	}

	void println(String strVar) {
		System.out.println(strVar);
	}
}